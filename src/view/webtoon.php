                     <h1>Les webtoons, c'est quoi ?</h1>
                     <img src="assets/image/webtoon.jpg" alt="post img" class="pull-left img-responsive postImg img-thumbnail margin10">
                     <article><p>
                     Les acheteurs français de BD en ligne ont l'habitude d'utiliser Sequencity et Izneo, qui vendent les bd traditionnelles en format numérique, 
                     type pdf. La lecture n'est pas toujours confortable, selon l'écran et les desssins, le prix est quasi le même que sur papier. A ce jour la 
                     formule numérique de la bd n' a pas fait d'énorme percée. Les Français.es seront-ils un jour prêts à adopter le format webtoon qui fait fureur 
                     en Corée-du-sud ?
                         </p>
    
                           <h2> Le webtoon, c'est de la bd qui sort des planches</h2>
                       <p> 
                       Le webtoon est à la Corée ce que le manga est au Japon. Le webtoon c’est la bd à scroller sur smartphone, par épisode très courts, et très 
                         nombreux, sur des applis dédiées. C'est le contraire du mode liseuse, où l’on tourne les pages virtuellement.  Ces histoires inspirent le cinéma 
                         pour des adaptations sur grand écran, mais aussi les séries tv, les jeux vidéo, et même l'édition papier. C'est tout le contraire de la BD 
                         française, qui se conçoit dans l'espace de la feuille et du livre, et qui est adaptée ensuite sur écran. Ce savoir-faire coréen commence à 
                         s'exporter aux Etats unis, en France  et en Chine.
                         Le webtoon s'achète au numéro. Au bout d'une dizaine d'épisode si le lectorat n'est pas au rendez-vous, la série s'arrête. 
                         </p>
                           <h2>Qui lit les webtoon ?</h2>
                       <p>
                       En Corée-du-sud, il y a 51 millions d'habitant.e.s, mais le chiffre d'affaires généré par le webtoon, 500 millions d'euros, 
                     est plus important que celui de la BD en France. Les estimations portent ce chiffre à 800 millions d'ici deux ans. 
                     Ce sont surtout les 20-30 ans qui lisent ces histoires, mais l'accès au webtoon est tellement facile, gratuit même sur 
                     Naver, le Google coréen, qu'il est difficile d'y échapper. La majorité des jeunes en consomment et les plus âgé.e.s y 
                     sont habitué.e.s car le webtoon a été utilisé à des fins publicitaires dès que l'usage du smartphone s'est développé.
                         </p>
                         <h3>Quel genre d'histoires ?</h3>
                          <br>
                         <iframe width="100%" height="480" src="//www.youtube.com/embed/XMkFqzMCN9g" frameborder="0" allowfullscreen></iframe>
                         
                         <p>
                         Viennent en tête la romance et  la fantasy. En Corée c'est un divertissement, mais cela n'empêche pas les auteurs d'aborder des sujets 
                         de société graves comme le harcèlement scolaire. L'une des auteur.e.s en vue du moment, Li Nare, est en train de travailler sur une 
                         histoire ayant pour cadre la guerre de Corée, sujet toujours brûlant pour ses compatriotes. 
                         </p>
                        <h4>Le secret du webtoon c'est le scroll</h4>
                       <p>
                       C'est un secret de polichinelle, car en Corée et au Japon notamment on n'imagine pas qu'il en soit autrement. Quand on lit sur écran on pousse le texte vers le haut pour le faire défiler. On scrolle. Que ce soit du texte, des images, ou un mix des deux.
                       En Asie, on scrolle pour le webtoon, pour les light novels, qui sont des petites histoires romantiques, pour le webroman, et pour le roman
                       En France une seule plateforme de lecture de la littérature a fait le pari du scroll c'est Glose.com. Les éditeurs publient des livres papier. Ils détiennent les droits de leurs auteurs. Du coup, aucune littérature, ou bd, n'est jamais conçue pour les écrans par nature, ni sous forme de feuilleton, ou pour des temps de lectures plus court. 
                       Le scroll n'existera en France que si une plateforme décide de jouer ce rôle d'éditeur, et de payer des auteurs pour créer des publications "à scroller". 
                         </p>
                         
                         <h4>Le webtoon existe en France, mais pas encore le webtoon français</h4>
                       <p>
                       Le webtoon existe en France par le biais de Delitoon, fondée par le Français Didier Borg,  avec le concours du coréen Kidari Ent. Delitoon vend le meilleur du webtoon coréen à des 15-30 ans, souvent des jeunes femmes.
                       La plateforme française a enregistré 800 000 visiteurs sur son titre phare, Honey Blood, et l'appli lancée début novembre a été installée par 25 000 utilisateurs.
                       Delitoon aimerait convaincre les auteurs français de se mettre au webtoon. Elle a donc invité Raphaëlle Marx, dite Raf, auteur connue en France pour la série manga Debaser, parue chez Ankama, d'aller en résidence dans un centre de formation à Busan.
                       Busan, la 2e ville de Corée du Sud après Séoul,  veut devenir the place-to-be dans le monde du webtoon, laissant à Séoul sa suprématie sur le cinéma d'animation.    
                       </p>
                     
                     
